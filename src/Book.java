public class Book {
    final String bookNo;
    final String title;
    final String author;
    final float price;
    final int stock;

    public Book(String bookNo, String title, String author, float price, int stock) {
        this.bookNo = bookNo;
        this.title = title;
        this.author = author;
        this.price = price;
        this.stock = stock;
    }
}
